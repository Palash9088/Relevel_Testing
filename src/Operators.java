public class Operators {
    public static void main(String[] args) {
        int num1 = 7, num2 = 8, num3 = 2;
        int output = num1 + num2;
        System.out.println("Output is " + output);

        output = num1 - num3;
        System.out.println("Output is " + output);

        output = num1 * num3;
        System.out.println("Output is " + output);

        float output1 = num1 / num2;
        System.out.println("Output is " + output1);

        output = num1 % num3;
        System.out.println("Output is " + output);

        output = num1 % num2;
        System.out.println("Output is " + output);


    }
}
