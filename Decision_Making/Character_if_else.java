public class Character_if_else {
    public static void main(String[] args) {
        // using if else
        char letter = 'b';
        if(letter == 'a' || letter == 'e' || letter == 'i' || letter == 'o' || letter == 'u')
        {
            System.out.println("Vowel");
        }
        else
        {
            System.out.println("Consonant");
        }
    }
}
