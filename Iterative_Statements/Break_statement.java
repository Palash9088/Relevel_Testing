public class Break_statement {
    public static void main(String[] args) {
        // Break jump statement based upon the condition i:e i = = 4

        int i;
        for (i = 0 ; i<=5 ; i++)
        {
            if (i == 4)
            {
                break;
            }
            System.out.println(i);
        }
    }
}
