public class Equality_Relational_operator {
    public static void main(String[] args) {
        int aa = 20, bb = 20;
        System.out.println(aa == bb); // equality (if equal it will show true)

        System.out.print("");

        System.out.println(aa != bb); // not equal (if not equal it will show false)

        // Relational Operator

        System.out.println( aa > bb); // Output False as it is equal

        System.out.print("");

        System.out.println(aa < bb); // Output False as it is equal

        // Less than or equal to <=

        System.out.println(aa <= bb); // Output true as both are equal to

        // Greater than or equal to >=

        System.out.println(aa >= bb); // Output true as both are equal to

    }
}
