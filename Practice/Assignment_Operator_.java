public class Assignment_Operator_ {
    public static void main(String[] args) {

        // Addition
        int num1 = 20;
        num1 += 30;
        System.out.println(num1);

        //Subtraction
        int num2 = 30;
        num2 -= 10;
        System.out.println(num2);

        int num3 = 40;
        num3 *= 10;
        System.out.println(num3);

        int num4 = 500;
        num4 /= 5;
        System.out.println(num4);

        int num5 = 500;
        num5 %= 10;
        System.out.println(num5);

    }
}
