public class if_else_num_positive_negative {
    public static void main(String[] args) {

        double num = 30;

        if (num > 0)
        {
            System.out.println(num +  "  is a positive number ");
        }
        else if (num < 0)
        {
            System.out.println( num +  " is negative number ");
        }
    }
}
