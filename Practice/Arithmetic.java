public class Arithmetic {
    public static void main(String[] args) {
        int num1 = 10 , num2 = 20;

        // Addition
        int sum = num1 + num2;
        System.out.println(sum);

        //Subtraction
        int sum1 = num1 - num2;
        System.out.println(sum1);

        // Multiplication
        int sum2 = num1 * num2;
        System.out.println(sum2);

        //Division
        int sum3 = num1 / num2;
        System.out.println(sum3);

        // Modulus (Gives remainder)
        int sum4 = num1 % num2;
        System.out.println(sum4);

    }
}
