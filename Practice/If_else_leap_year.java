public class If_else_leap_year {
    public static void main(String[] args) {
        // check for leap year

        int year = 2021;
        if (year % 2 == 0)
        {
            System.out.println( year + " Year is leap year ");
        }
        else
        {
            System.out.println(year + " Year is not a leap year");
        }
    }

}
