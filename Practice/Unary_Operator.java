public class Unary_Operator {
    public static void main(String[] args) {
        int a=10;
        a++;
        System.out.println(a);

        int b=20;
        b--;
        System.out.println(b);

        int c = +30;
        System.out.println(+c);

        int d = 30;
        System.out.println(-d);

    }
}
